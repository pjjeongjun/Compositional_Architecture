function [lc,cmap,ic] = linecolors(nc,cmap)
% linecolors extract ordered line colors from colormap
%
% Inputs:
%  nc: number of colors to be extraced
%  cmap: colormap ([ncol 3] or string) 
%        custom maps: 'shortjet','blue','darkgray'
%
% [lc,cmap,ic] = linecolors(nc,cmap)

if nargin<2 || isempty(cmap)
    cmap = 'shortjet';
end

if ischar(cmap)
    switch cmap
        case 'shortjet'
            cmap = shortjet();
        case 'blue'
            cmap = blue();
        case 'darkgray'
            cmap = darkgray();
        otherwise
            hf=figure;
            cmap = colormap(cmap);
            close(hf);
    end
end

ncol = size(cmap,1);
ic = round(linspace(1,ncol,nc));
lc = cmap(ic,:);

return

